import socket

sock = socket.socket()  #создание сокета
sock.bind (('', 9090))  #Выберем любой порт от 0 до 65535. Порты от 0 до 1023 требуют определенных привилегий
sock.listen(1)  #Запуск режима прослушивания
conn, addr = sock.accept()  #Принятие подключения 
print(addr)
data = conn.recv(1024).decode()  #Для получения данных от клиента небольшими частями, а именно 1024 Байт
print(data)

conn.close()  #Закрываем соединение
